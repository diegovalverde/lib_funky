use assert, diagonals, antidiagonals


      test_diagonals():
       say('====== get diagonals =======')
       M      <- [[3, 4, 5,  7,  5,  4, 3],
                  [4, 6, 8,  9,  8,  6, 4],
                  [5, 8, 11, 13, 11, 8, 5],
                  [5, 8, 11, 13, 11, 8, 5],
                  [4, 6, 8,  9,  8,  6, 4],
                  [3, 4, 5,  7,  5,  4, 3]]
      
       assert(diagonals(M), [[4 ,8 ,13 ,11 ,6 ],
                              [5 ,9 ,11 ,8 ],
                              [7 ,8 ,8 ],
                              [5 ,6 ],
                              [4 ],
                              [3 ,6 ,11 ,13 ,8 ,4 ],
                              [4 ,8 ,11 ,9 ,5 ],
                              [5 ,8 ,8 ,7 ],
                              [5 ,6 ,5 ],
                              [4 ,4 ],
                              [3 ]])


       assert(diagonals([[1,0],[0,1]]), [[0], [1,1], [0]]  )

       assert(diagonals([[-2,  5,  3,  2],
                         [ 9, -6,  5,  1],
                         [ 3,  2,  7,  3],
                         [-1,  8, -4,  8]]),
                         [[5,5,3], [3,1], [2], [-2,-6,7,8],[9,2,-4], [3,8], [-1] ])

      assert(antidiagonals([[-2,  5,  3,  2],
                            [ 9, -6,  5,  1],
                            [ 3,  2,  7,  3],
                            [-1,  8, -4,  8]]), [[3 ,-6 ,3 ], 
                                                 [9 ,5 ],
                                                 [-2 ],
                                                 [-1 ,2 ,5 ,2 ],
                                                 [8 ,7 ,1 ],
                                                 [-4 ,3 ],
                                                 [8 ]])
      
       1.

      
