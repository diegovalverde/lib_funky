use assert

test_ranges():
     say('====== Test Ranges =====')
     P <- [[1,2,3] , [4,5,6] , [3,3,3] , 4 , 5 , 6 , 7 ]
      say(P)
      assert(P[3], 4)
      assert(P[3..6], [4,5,6,7])
      assert(P[2..6], [[3,3,3],4,5,6,7])
      assert(P[0..1], [[1,2,3], [4,5,6]])
      assert(P[0 .. 2], [[1,2,3], [4,5,6], [3,3,3]])
      assert(P[0 .. 2, 1..2], [[4,5,6], [3,3,3]])
      assert(P[1],[4,5,6])

      
      assert(P[1,2],6)
      assert(P[1, 1..2],[5,6])
      
      assert(P[0..0], [[1,2,3]] )
      assert(P[1..-1], [ [4,5,6] , [3,3,3] , 4 , 5 , 6 , 7 ])
      
    
